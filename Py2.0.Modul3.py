class AppConfig:
    _instance = None # —Ö—Ä–∞–Ω–∏—Ç–µ–ª—å –µ–¥–∏–Ω—Å—Ç–≤–µ–Ω–Ω–æ–≥–æ —ç–∫–∑–µ–º–ø–ª—è—Ä–∞

    def __new__(cls):
        if cls._instance is None: # –ï—Å–ª–∏ —ç–∫–∑–µ–º–ø–ª—è—Ä —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç, –≤–µ—Ä–Ω–µ–º –µ–≥–æ
            cls._instance = super(AppConfig, cls).__new__(cls)
            cls._instance.settings = {
                "theme": "Dark",
                "volume": 80
            }
        return cls._instance

    def get_setting(self, key): # –ø–æ–∑–≤–æ–ª—è–µ—Ç –ø–æ–ª—É—á–∏—Ç—å —Ç–µ–∫—É—â–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–π –Ω–∞—Å—Ç—Ä–æ–π–∫–∏
        return self.settings.get(key)

    def update_setting(self, key, value): # –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–ª—è –∏–∑–º–µ–Ω–µ–Ω–∏—è –∑–Ω–∞—á–µ–Ω–∏–π –Ω–∞—Å—Ç—Ä–æ–µ–∫
        self.settings[key] = value

